<h1>Instructions</h1>
<h2>Map</h2>
<section>
    <ul>
        <li>Scroll to zoom / dezoom on the map</li>
        <li>Click and drag to move the map around </li>
        <li>
            Hold the <kbd> Ctrl </kbd> or <kbd> ⌘ </kbd> key while dragging to rotate the map around
        </li>
        <li>Hold the <kbd> ⇧ Shift </kbd> to change the tilt</li>
        <li>
            Right-click anywhere on the map or elements on the map to bring a
            popup that will enable to:
            <ul>
                <li>Create points / labels</li>
                <li>Create hand-drawn paths</li>
                <li>Change the style of the map elements</li>
            </ul>
        <li>
            Double clicking on a label or a legend title will enable its
            edition.
        </li>
    </ul>
</section>
<h2>Project</h2>
<p>
    The state of the current project is preserved locally in your browser. To
    transfer it or make sure to keep it, you can save it (<code>
        Project > Save
    </code>
    at the bottom of the map). You can also reset the current project to the initial
    state, clicking the <code> Reset </code> button.
</p>
<h2>Add a font</h2>
<p>
    It is possible to add a font by clicking the <code> Add font </code> button
    at the bottom of the map, and chosing a font on your local storage. <br />
    Then, it will be possible to chose it from the
    <code>inline-style-editor</code>
    popup (when editing text style), in the <strong> font-family </strong> section.
</p>
<h2>While editing a hand-drawn path</h2>
<section>
    <ul>
        <li>Click and drag anywhere on the path to move the whole path</li>
        <li><kbd> Ctrl </kbd> + click anywhere on the path to add a point</li>
        <li>
            <kbd> Ctrl </kbd> + click on a point to remove it. Removing a point when
            there are only two will delete the path
        </li>
    </ul>
</section>

<h2>Export</h2>
<section>
    When you are satisfied with your creation, you can export it with the
    "Export" button at the bottom of the map. <br />
    Exporting as SVG is recommanded (it is the main usage of this tool). It will
    be optimized as much as possible (shape and data-wise), and will follow the preferences
    set in the export modal. <br />
    You can also export it as a png image if you prefer.
</section>
